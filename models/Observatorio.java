package com.example.mariomarcillo.proyecto.models;

public class Observatorio {


    private String altitud;

    private String fecha_de_instalaci_n;

    private String id;

    private String latitud;

    private String longitud;

    private String nombre;

    private String ovs;

    private String tipo_de_estaci_n;

    private String volc_n;

    public String getAltitud() {
        return altitud;
    }

    public void setAltitud(String altitud) {
        this.altitud = altitud;
    }

    public String getFechaDeInstalaciN() {
        return fecha_de_instalaci_n;
    }

    public void setFechaDeInstalaciN(String fechaDeInstalaciN) {
        this.fecha_de_instalaci_n = fechaDeInstalaciN;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getOvs() {
        return ovs;
    }

    public void setOvs(String ovs) {
        this.ovs = ovs;
    }

    public String getTipoDeEstaciN() {
        return tipo_de_estaci_n;
    }

    public void setTipoDeEstaciN(String tipoDeEstaciN) {
        this.tipo_de_estaci_n = tipoDeEstaciN;
    }

    public String getVolcN() {
        return volc_n;
    }

    public void setVolcN(String volcN) {
        this.volc_n = volcN;
    }

}

